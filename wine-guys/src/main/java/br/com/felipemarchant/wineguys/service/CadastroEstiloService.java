package br.com.felipemarchant.wineguys.service;

import java.util.Optional;

import br.com.felipemarchant.wineguys.repository.Estilos;
import br.com.felipemarchant.wineguys.service.exception.NomeEstiloJaCadastradoException;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

import br.com.felipemarchant.wineguys.model.Estilo;

@Service
public class CadastroEstiloService {

	@Autowired
	private Estilos estilos;
	
	@Transactional
	public Estilo salvar(Estilo estilo) {
		Optional<Estilo> estiloOptional = estilos.findByNomeIgnoreCase(estilo.getNome());
		if (estiloOptional.isPresent()) {
			throw new NomeEstiloJaCadastradoException("Nome do estilo j√° cadastrado");
		}
		
		return estilos.saveAndFlush(estilo);
	}
	
}
